    private void txtNombreServicioKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtNombreServicioKeyTyped
        
       int numerocaracteres = 80; 
        
        if(txtNombreServicio.getText().length() >= numerocaracteres) {
            evt.consume();
   
            JOptionPane.showMessageDialog(rootPane, "El Nombre no puede tener mas de 80 Caracteres");
        }
        
    }//GEN-LAST:event_txtNombreServicioKeyTyped

    private void txtPrecioServiKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtPrecioServiKeyTyped
        
          char c = evt.getKeyChar(); 
        
        if(!Character.isDigit(c)){
            evt.consume();
        }
        
        int numerocaracteres = 30; 
        
        if(txtPrecioServi.getText().length() >= numerocaracteres) {
            evt.consume();
   
            JOptionPane.showMessageDialog(rootPane, "El precio del Servicio es muy largo");
        }
        
        
        
    }//GEN-LAST:event_txtPrecioServiKeyTyped

