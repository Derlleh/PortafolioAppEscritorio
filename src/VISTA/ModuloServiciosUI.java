    private void btnEditarActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActualizarActionPerformed
        
        int i = JOptionPane.showConfirmDialog(this, "¿Estas seguro que deseas actualizar los datos del Servicio?"); 
            if(i==0) {
                
                if(txtCodigoServi.getText().isEmpty())
                {
                     String mensaje = "debe seleccionar un Codigo para editar"; 
                      JOptionPane.showMessageDialog(null, mensaje);
                }
                else{
                    
                  Servicio modelS = new Servicio(); 
                  ServicioDAO daoS = new ServicioDAO(); 
                  
                  modelS.setCodServicio(Integer.parseInt(txtCodigoServi.getText()));
                  modelS.setNombreServicio(txtNombreServicio.getText());
                  modelS.setPrecioServicio(Integer.parseInt(txtPrecioServi.getText()));
                  
                  if(daoS.ModificarServicio(modelS))
                  {
                      String mensaje = "Servicio Modificado con exito"; 
                      JOptionPane.showMessageDialog(null, mensaje);
                      ListarServicios();
                      habilitarBotonEditar();
                      
                  }
                    
                }
                
                
            }else if(i==1)
            {
                 JOptionPane.showMessageDialog(this, "Se cancelo la actualizacion de datos del Servicio");
            }
        
        
        
    }//GEN-LAST:event_btnEditarActualizarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        
        int i = JOptionPane.showConfirmDialog(this, "¿Estas seguro que deseas eliminar el Servicio?"); 
       
        if(i==0){
         
                 
                  Servicio modelS = new Servicio(); 
                  ServicioDAO daoS = new ServicioDAO(); 
                  
                  modelS.setCodServicio(Integer.parseInt(txtCodigoServi.getText()));
                  modelS.setNombreServicio(txtNombreServicio.getText());
                  modelS.setPrecioServicio(Integer.parseInt(txtPrecioServi.getText()));
                  if(daoS.EliminarServicio(modelS))
                  {
                      JOptionPane.showMessageDialog(null, "Se Elimino al Servicio con exito");  
                      ListarServicios();
                      LimpiarServicios();
                      habilitarBotonEliminar();
                  }
            
            
        }else if(i==1)
        {
            JOptionPane.showMessageDialog(this, "Se cancelo la eliminacion del Servicio"); 
        }
        
        
    }//GEN-LAST:event_btnEliminarActionPerformed

