    private void btnAgregarServiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarServiActionPerformed
        
          int i = JOptionPane.showConfirmDialog(this, "¿Estas seguro que deseas Registrar este Servicio"); 
          if(i==0){
              
              if(txtCodigoServi.getText().isEmpty() ||
                  txtNombreServicio.getText().isEmpty() ||
                      txtPrecioServi.getText().isEmpty()) 
              {
                  JOptionPane.showMessageDialog(null, "Llene todos los campos");
              }
              else{
                  
                  Servicio modelS = new Servicio(); 
                  ServicioDAO daoS = new ServicioDAO(); 
                  
                  modelS.setCodServicio(Integer.parseInt(txtCodigoServi.getText()));
                  modelS.setNombreServicio(txtNombreServicio.getText());
                  modelS.setPrecioServicio(Integer.parseInt(txtPrecioServi.getText()));
                  
                  if(daoS.registroServicio(modelS))
                  {
                      JOptionPane.showMessageDialog(null, "Se registro al Servicio con exito");
                      ListarServicios();
                      LimpiarServicios();
                      habilitarBotonGuardar();
                  }
              }
                      
              
              
          
          
              
          }else if(i==0)
          {
               JOptionPane.showMessageDialog(this, "Se cancelo el registro del Servicio");
          }
        
        
    }//GEN-LAST:event_btnAgregarServiActionPerformed

