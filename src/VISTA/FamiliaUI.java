    private void btnGuardarFamiliaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarFamiliaActionPerformed
      
          int i = JOptionPane.showConfirmDialog(this, "¿Estas seguro que deseas Registrar una nueva Familia de Productos?"); 
          if(i==0){
              
              if(txtIDcodigoFamilia.getText().isEmpty() || 
                      txtNombreFamilia.getText().isEmpty()) 
              {
                  JOptionPane.showMessageDialog(null, "Llene todos los campos");
              }
              else
              {
                  Familia modelF = new Familia(); 
                  FamiliaDAO daoF = new FamiliaDAO(); 
                  
                  modelF.setCodFamilia(Integer.parseInt(txtIDcodigoFamilia.getText()));
                  modelF.setNombreFamilia(txtNombreFamilia.getText());
                  
                  if(daoF.registroFamilia(modelF))
                  {
                      JOptionPane.showMessageDialog(null, "Se registro la Nueva Familia con exito");
                      ListarFamilias();
                      LimpiarFamilia();
                      habilitarBotonGuardarFamilia();
                     
                      
                  }
                  
                  
              }
                  
              
              
              
          } else if(i==1){
              
              JOptionPane.showMessageDialog(this, "Se cancelo el registro de la Familia del Producto");
              
              
          }
        
        
        
    }//GEN-LAST:event_btnGuardarFamiliaActionPerformed

