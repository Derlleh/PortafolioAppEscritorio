    private void btnEditarFamiliaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarFamiliaActionPerformed
      
            int i = JOptionPane.showConfirmDialog(this, "¿Estas seguro que deseas actualizar los datos de la Familia?"); 
            if(i==0)
            {
                
                if(txtIDcodigoFamilia.getText().isEmpty())
                {
                    String mensaje = "Debe tener un codigoc seleccionado";
                    JOptionPane.showMessageDialog(null, mensaje);
                }
                else
                {
                  Familia modelF = new Familia(); 
                  FamiliaDAO daoF = new FamiliaDAO(); 
                  
                  modelF.setCodFamilia(Integer.parseInt(txtIDcodigoFamilia.getText()));
                  modelF.setNombreFamilia(txtNombreFamilia.getText());
                  
                 daoF.ModificarFamilia(modelF); 
                 
                 String mensaje = "Nombre de Familia Actualizado con exito"; 
                 ListarFamilias();
                 habilitarBotonEditarFamilia();
                    
                    
                }
                
                
                
            }else if(i==1)
            {
                  JOptionPane.showMessageDialog(this, "Se cancelo la actualizacion de datos de la Familia de Productos");
            }
        
        
    }//GEN-LAST:event_btnEditarFamiliaActionPerformed

    private void btnEliminarFamiliaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarFamiliaActionPerformed
    
          int i = JOptionPane.showConfirmDialog(this, "¿Estas seguro que deseas eliminar la familia?"); 
          if(i==0){
              
                  Familia modelF = new Familia(); 
                  FamiliaDAO daoF = new FamiliaDAO(); 
                  
                  modelF.setCodFamilia(Integer.parseInt(txtIDcodigoFamilia.getText()));
                  modelF.setNombreFamilia(txtNombreFamilia.getText());
                  
                  if(daoF.eliminarFamilia(modelF))
                  {
                      JOptionPane.showMessageDialog(null, "Se Elimino la Familia con exito");
                      ListarFamilias();
                      LimpiarFamilia();
                      habilitarBotonEliminarFamilia();
                  }
              
              
              
          }else if(i==1)
          {
                JOptionPane.showMessageDialog(this, "Se cancelo la eliminacion de la familia de productos");
          }
        
        
        
    }//GEN-LAST:event_btnEliminarFamiliaActionPerformed

